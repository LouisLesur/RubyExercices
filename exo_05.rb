puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

puts 3 + 2 < 5 - 7 #calcule d'un égalité, la réponse se traduira par un "true" ou un "false" en fonction du résultat du calcul

puts "Ça fait combien 3 + 2 ? #{3 + 2}" #opération numérique
puts "Ça fait combien 5 - 7 ? #{5 - 7}" #opération numérique

