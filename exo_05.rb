puts "C'est drôle ça, faisons-en plus :"

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" #calcule d'un égalité, la réponse se traduira par un "true" ou un "false" en fonction du résultat du calcul
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" #calcule d'un égalité, la réponse se traduira par un "true" ou un "false" en fonction du résultat du calcul
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}" #calcule d'un égalité, la réponse se traduira par un "true" ou un "false" en fonction du résultat du calcul

puts "le groupe # {} permet de réaliser des opérations numériques directement a la suite d'un élément"
